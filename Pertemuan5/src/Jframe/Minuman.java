    private void bdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdActionPerformed
        // TODO add your handling code here:                                        
        try {
            String id = tid.getText();
            String sql = "DELETE FROM dataminuman WHERE id_minuman='" + id + "'";
            Connection conn = Koneksi.getConnection();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus");

            // Refresh tabel
            tampilData();

            // Kosongkan textfield
            tid.setText("");
            tnama.setText("");
            cbka.setSelectedIndex(0);
            tha.setText("");
            cbu.setSelectedIndex(0);

            tid.setEditable(true);

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_bdActionPerformed

    private void biActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_biActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(tid.getText());
            String nama = tnama.getText();
            String kategori = cbka.getSelectedItem().toString();
            int harga = Integer.parseInt(tha.getText());
            String ukuran = cbu.getSelectedItem().toString();

            Connection conn = Koneksi.getConnection();
            String sql = "INSERT INTO dataminuman (id_minuman, nama, kategori, harga, ukuran) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, id);
            ps.setString(2, nama);
            ps.setString(3, kategori);
            ps.setInt(4, harga);
            ps.setString(5, ukuran);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
            
            tid.setText("");
            tnama.setText("");
            cbka.setSelectedIndex(0);
            tha.setText("");
            cbu.setSelectedIndex(0);

            tid.setEditable(true);   // Kosongkan textfield
            
            tampilData();
        } catch (NumberFormatException | SQLException e) {
            System.out.println("Gagal insert: " + e.getMessage());
        }
    }//GEN-LAST:event_biActionPerformed

