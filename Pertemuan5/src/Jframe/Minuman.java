    private void buActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE dataminuman SET nama = ?, kategori = ?, harga = ?, ukuran = ? WHERE id_minuman = ?";
            Connection conn = Koneksi.getConnection();
            PreparedStatement pst = conn.prepareStatement(sql);

            // Ambil dari textfield
            pst.setString(1, tnama.getText());
            pst.setString(2, cbka.getSelectedItem().toString());
            pst.setInt(3, Integer.parseInt(tha.getText()));
            pst.setString(4, cbu.getSelectedItem().toString());
            pst.setInt(5, Integer.parseInt(tid.getText())); // ID yang sudah auto increment

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate");

            // Refresh tabel
            tampilData();

            // Kosongkan textfield
            tid.setText("");
            tnama.setText("");
            cbka.setSelectedIndex(0);
            tha.setText("");
            cbu.setSelectedIndex(0);

            tid.setEditable(true);

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_buActionPerformed

